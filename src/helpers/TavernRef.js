
export function getTavernKR(tavernEN) {

    switch (tavernEN) {

    case 'capital':
        return '부도'
    case 'holy':
        return '성도'
    case 'sage':
        return '현자의탑'
    case 'maze':
        return '미궁산맥'
    case 'lake':
        return '호수도시'
    case 'soul':
        return '정령섬'
    case 'fire':
        return '화염구령'
    case 'seaBreeze':
        return '해풍의항구'
    case 'ringGacha':
        return '링가챠'
    case 'ringTrade':
        return '링교환'
    case 'daybreakOcean':
        return '새벽대해'
    case 'beast':
        return '수인의대륙'
    case 'sin':
        return '죄의대륙'
    case 'ephemerality':
        return '박명의대륙'
    case 'iron':
        return '철연의대륙'
    case 'book':
        return '서가'
    case 'chronicle':
        return '연대기대륙'
    case 'lemures':
        return '레무레스섬'
    case 'holyKingdom':
        return '성왕국'
    case 'sage2':
        return '현자의탑 3부'
    case 'lake2':
        return '호수도시 3부'
    case 'soul2':
        return '정령섬 3부'
    case 'fire2':
        return '화염구령 3부'
    default:
        return '주점'

    }
    

}